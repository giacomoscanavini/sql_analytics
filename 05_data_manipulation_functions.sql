/*  Aggregate functions 
    More functions: https://www.w3schools.com/sql/sql_ref_sqlserver.asp  
    Note that Aggregate functions migth require GROUP BY  */
SELECT
    MIN(amount) AS smallest_order, -- MIN over a whole column
    MAX(amount) AS largest_order   -- MAX over a whole column
FROM Orders;

SELECT
    customer_id,
    SUM(amount) AS total_amount   -- SUM is applied to each aggregated group generated by GROUP BY
FROM Orders
GROUP BY customer_id;

SELECT 
    customer_id,
    AVG(amount) AS mean_amount -- AVG is applied to each aggregated group generated by GROUP BY
FROM Orders
GROUP BY customer_id
HAVING AVG(amount) > 150; -- HAVING is used to filter among the groups after aggregation

