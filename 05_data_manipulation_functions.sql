/* DATA MANIPULATION FUNCTIONS */
/* string manipulations 
    More string functions: https://www.w3schools.com/sql/sql_ref_sqlserver.asp  */
SELECT UPPER(firstName) AS upperName
SELECT LOWER(firstName) AS lowerName
FROM Employees

SELECT LTRIM(firstName) AS cleanFirstName -- remove spaces on the left
SELECT RTRIM(firstName) AS cleanFirstName -- remove spaces on the right
SELECT TRIM(firstName) AS cleanFirstName  -- remove any space from right or left
FROM Employees


/* date manipulation 
    More date functions: https://www.w3schools.com/sql/sql_ref_sqlserver.asp  */
SELECT 
    from_date,
    YEAR(from_date) AS year,
    MONTH(from_date) AS month,
    DAY(from_date) AS day,
    DATE_FORMAT(from_date, '%d.%m.%y') AS new_format
FROM Calendar


